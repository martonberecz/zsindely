              The much-anticipated release of Timeline for Brands was rolled out today by Facebook and there are a few things you need to know if you are a Brand manager or Business owner.
              The newest feature has rolled out for preview across the network, and if you don’t upgrade before March 30, your page will be automatically converted to the new style. Similar to personal profiles, Timeline for Brands has design beauty potential. Facebook has added features to Brands that personal profiles don’t have – such as public storylines and new admin capabilities, which I’ll discuss in a moment.
      </p>

      <h3 class="title"> <b>Other Important Changes To Pages</b></h3>                
            <p class="blog-body">
              <span class="view-link-color">1. Cover Images</span> – The new look for Brands includes a large 850px-wide image (similar to personal profiles) at the top of the page, with the page itself separated into two columns by a “timeline”. This affords companies a beautiful visual way to share and engage with their visitors, even more so than before. The one caveat to your brand’s cover image? Facebook’s rules stipulate you may not promote your products or services there. Yep…you are reading that correctly – no pricing information, no discount or percent off information, no web address, no contact information, no encouraging people to like your page, etc.
              <br><br>
              <span class="view-link-color">2. Reduced Tab Visibility</span> – Where as before you could have as many tabs as you wanted in the sidebar, now you’ll be forced to choose the top three you want to show under the cover image. Choose carefully and wisely – as there are only three tabs visible at any given time. The one tab you cannot change is the “Photo” tab, to see more than the first four, users will have to expand the tab panel by clicking on a drop-down menu.
              <br><br>                  
              <span class="view-link-color">3. Feature Posts </span>– You will now be able to feature posts for 7 days by “pinning” them to the top of your Timeline (akin to posting a ‘sticky’ post in a forum or on a blog). If you have a promotion or special offers, this is an opportunity for it to shine on the page and not get lost in the daily shuffle. This is also where you will want to focus your marketing strategy to point visitors to contests, promotions, other tabs, etc. It will be interesting to see how marketers use this particular feature.
              <br><br>                  
              <span class="view-link-color">4. Private Messages </span>– With the new update, users will be able to leave private messages for brands and brands can interact with them outside of the Timeline. It will be easy to clutter up the Timeline with customer inquiries and notes, so the addition of messaging is going to be really beneficial for page admins.  
            </p>

            <h3 class="title"> <b>What We Recommend to Our Clients:</b></h3>                
            <p class="blog-body">
              <span class="view-link-color">Don’t convert yet. </span>The most important “next step” is for you to prepare your Timeline Cover Photo and decide what apps/tabs are going to be visible on the home page. You can see a preview of your Timeline by going to your page and walking through the tutorial. Do not publish the changes – play around with it, but make sure that before you publish, you have a nice cover photo, and have cleaned up your timeline. In addition, there are bound to be a few bugs to be worked out, let Facebook handle those while you are getting used to the new highly visual format of your page.
